#!/usr/bin/env python3
"""
Fetch real FIFA World Cup 2026 results and write them to js/results.js.

Runs inside GitHub Actions on a schedule (see .github/workflows/update-results.yml).
Source: fixturedownload.com public JSON feed (no API key required).

Usage:
    python scripts/fetch_results.py                 # fetch from the live feed
    python scripts/fetch_results.py --from-file f   # offline test with a saved feed
"""

import json
import sys
import urllib.request
from pathlib import Path

FEED_URL = "https://fixturedownload.com/feed/json/fifa-world-cup-2026"
OUT_PATH = Path(__file__).resolve().parent.parent / "js" / "results.js"

# feed team names -> names used by the model (js/data.js)
NAME_MAP = {
    "USA": "United States",
    "Korea Republic": "South Korea",
    "Türkiye": "Turkey",
    "Côte d'Ivoire": "Ivory Coast",
    "Côte d’Ivoire": "Ivory Coast",
    "Cabo Verde": "Cape Verde",
    "Curaçao": "Curacao",
    "IR Iran": "Iran",
    "Congo DR": "DR Congo",
    "DR Congo": "DR Congo",
    "Bosnia-Herzegovina": "Bosnia and Herzegovina",
}


def norm(name: str) -> str:
    return NAME_MAP.get((name or "").strip(), (name or "").strip())


def fetch_feed() -> list:
    req = urllib.request.Request(FEED_URL, headers={"User-Agent": "wc-results-bot/1.0"})
    with urllib.request.urlopen(req, timeout=30) as r:
        return json.loads(r.read().decode("utf-8"))


def transform(feed: list) -> list:
    out = []
    for m in feed:
        hs, as_ = m.get("HomeTeamScore"), m.get("AwayTeamScore")
        if hs is None or as_ is None:
            continue  # not played yet
        h, a = norm(m.get("HomeTeam")), norm(m.get("AwayTeam"))
        if not h or not a or "announced" in h.lower() or "announced" in a.lower():
            continue
        out.append({
            "n": m.get("MatchNumber"),
            "r": m.get("RoundNumber"),
            "date": (m.get("DateUtc") or "")[:10],
            "h": h,
            "a": a,
            "hs": int(hs),
            "as": int(as_),
        })
    out.sort(key=lambda x: (x["n"] or 0))
    return out


def write_results(results: list) -> bool:
    body = (
        "// Auto-generated by scripts/fetch_results.py via GitHub Actions - do not edit.\n"
        "// Source: fixturedownload.com FIFA World Cup 2026 feed.\n"
        f"const WC_RESULTS = {json.dumps(results, separators=(',', ':'))};\n"
    )
    old = OUT_PATH.read_text(encoding="utf-8") if OUT_PATH.exists() else ""
    if old == body:
        return False
    OUT_PATH.write_text(body, encoding="utf-8")
    return True


def main() -> int:
    if len(sys.argv) >= 3 and sys.argv[1] == "--from-file":
        feed = json.loads(Path(sys.argv[2]).read_text(encoding="utf-8"))
    else:
        feed = fetch_feed()
    results = transform(feed)
    changed = write_results(results)
    print(f"completed matches: {len(results)} | results.js {'updated' if changed else 'unchanged'}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
